# 1. Toán tử số học (Arithmetic Operators)
# + : Cộng
# - : Trừ
# * : Nhân
# / : Chia (trả về số thực)
# // : Chia lấy phần nguyên
# % : Chia lấy phần dư (modulo)
# ** : Lũy thừa

# 2. Toán tử so sánh (Comparison Operators)
# == : Bằng
# != : Khác
# > : Lớn hơn
# < : Nhỏ hơn
# >= : Lớn hơn hoặc bằng
# <= : Nhỏ hơn hoặc bằng

# 3. Toán tử logic (Logical Operators)
# and : Và (cả hai đều đúng)
# or : Hoặc (ít nhất một đúng)
# not : Phủ định

# 4. Toán tử gán (Assignment Operators)
# = : Gán
# += : Cộng rồi gán
# -= : Trừ rồi gán
# *= : Nhân rồi gán
# /= : Chia rồi gán
# //= : Chia lấy nguyên rồi gán
# %= : Chia lấy dư rồi gán
# **= : Lũy thừa rồi gán
# &= : AND bit rồi gán
# |= : OR bit rồi gán
# ^= : XOR bit rồi gán
# >>= : Dịch phải rồi gán
# <<= : Dịch trái rồi gán

# 5. Toán tử Bitwise (Bitwise Operators)
# & : AND bit
# | : OR bit
# ^ : XOR bit
# ~ : NOT bit (đảo bit)
# << : Dịch trái
# >> : Dịch phải

# 6. Toán tử thành viên (Membership Operators)
# in : Kiểm tra có trong
# not in : Kiểm tra không có trong

# 7. Toán tử định danh (Identity Operators)
# is : Kiểm tra cùng đối tượng
# is not : Kiểm tra khác đối tượng

# 8. Toán tử Walrus (Assignment Expression)
# := : Gán và trả về giá trị (từ Python 3.8)

# 9. Toán tử ma trận (Matrix Operator)
# @ : Nhân ma trận (dùng với NumPy)